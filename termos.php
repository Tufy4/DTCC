<h6>POLÍTICA DE ENTREGA </h6>
<p> 
O prazo para a entrega dos produtos é informado durante o processo de compra e contabilizado em dias úteis. No caso das entregas feitas pelos Correios, é importante observar o seguinte procedimento:
<p>
Haverá 2 (duas) tentativas de entrega em dias úteis, sendo a primeira na data de chegada na unidade de distribuição. Sendo impossível a entrega devido a casa fechada ou ausência de pessoa civilmente capaz, a encomenda ficará disponível para entrega na agência mais próxima do endereço do destinatário, durante sete (7) dias corridos. Após esse prazo, a encomenda será devolvida à IRON BODY. Se isso ocorrer, o próximo envio irá gerar um novo custo de frete, que é de responsabilidade do cliente.
A entrega no balcão da unidade postal é feita apenas ao próprio destinatário, ou a pessoa por ele autorizada, no campo apropriado do aviso de chegada deixado pelo carteiro.
