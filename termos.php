O prazo de entrega informado durante o procedimento de compra do produto leva em consideração o estoque, a região, o processo de emissão da nota fiscal e o tempo de preparo do produto.
Os pedidos com pagamento aprovado até às 13h em dias úteis são processados no mesmo dia. Pedidos com o pagamento aprovado após esse horário tem seu processamento feito apenas no próximo dia útil.
Em finais de semana e feriados, o prazo também passa a contar a partir do próximo dia útil. Em todos os casos, o dia da postagem não é considerado no cálculo do prazo de entrega.
</p>
O valor do frete da entrega é calculado com base no local de entrega, peso e dimensões do produto.
Algumas localidades apresentam restrições de entrega, definidas pelos próprios Correios e Transportadora. Isso pode acarretar atraso ou até mesmo a impossibilidade da entrega e, nesses casos, a encomenda fica disponível para retirada pelo cliente na agência mais próxima ou retorna para o nosso centro de distribuição.
No caso de encomenda extraviada ou perdida pelos Correios, caso está seja comprovada, a IRON BODY se responsabiliza por enviar um novo produto ao cliente sem custo adicional.
A IRON BODY não autoriza os Correios e a Transportadora a:
Entrar no domicílio;
Abrir a embalagem do produto;
Realizar entrega em endereço diferente do que consta na Nota Fiscal de Postagem;
Realizar entrega a menor de idade ou sem documento de identificação
A IRON BODY não se responsabiliza pela retenção de mercadorias na SEFAZ (Secretaria de Estado da Fazenda) quando está se dever exclusivamente a pendências do cliente, sendo, portanto, necessário seu comparecimento no posto fiscal para que a mercadoria seja liberada, tendo em vista que nestes casos as informações referentes a liberações e pagamentos só são passadas aos interessados.
